#!/bin/bash

# U-15: 소유자가 존재하지 않는 파일 및 디렉터리 점검

echo "=============================="
echo " U-15: 파일 및 디렉터리 소유자 설정 점검"
echo "=============================="

# 소유자 또는 그룹이 존재하지 않는 파일 및 디렉터리 검색
RESULT=$(find / \( -nouser -o -nogroup \) -xdev -ls 2>/dev/null)

if [ -z "$RESULT" ]; then
    echo "[양호] 소유자가 존재하지 않는 파일 및 디렉터리가 없습니다."
else
    echo "[취약] 소유자가 존재하지 않는 파일 및 디렉터리가 발견되었습니다."
    echo ""
    echo "[발견된 파일 목록]"
    echo "$RESULT"
    echo ""
    echo "[조치 방법]"
    echo "  - 불필요한 파일 제거: rm <파일명>"
    echo "  - 소유자 변경: chown <사용자> <파일명>"
    echo "  - 그룹 변경: chgrp <그룹> <파일명>"
fi

echo "=============================="
